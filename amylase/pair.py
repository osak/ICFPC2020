from collections import Counter


def main():
    ls = data.splitlines()
    nums = []
    for r in range(8):
        for c in range(8):
            l = ls[r * 8 + c]
            print(r, c, l, int(l, 2))
            nums.append(int(l, 2))

    freqs = Counter(nums)
    for d, freq in freqs.items():
        print(d, freq)


